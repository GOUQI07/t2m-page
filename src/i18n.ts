export type TranslationKey = keyof typeof messages['en-US'];

export function getInitialLocale(): Locale {
  const saved = localStorage.getItem(LOCALE_STORAGE_KEY);
  return saved === 'zh-CN' || saved === 'en-US' ? saved : 'zh-CN';
}

export function translate(locale: Locale, key: TranslationKey, values: Record<string, string | number> = {}) {
  const template = messages[locale][key] || messages['en-US'][key] || key;
  return Object.entries(values).reduce(
    (text, [name, value]) => text.replaceAll(`{${name}}`, String(value)),
    template
  );
}
